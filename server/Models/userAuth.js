const { Sequelize, DataTypes } = require('sequelize');
const { sequelize } = require('../db/userAuthDb');

const userAuth = sequelize.define('userAuth', {
    id: {
        type: DataTypes.BIGINT,
        autoIncrement: true,
        primaryKey: true
    },
    name: {
        type: DataTypes.STRING,
        allowNull: false
    },
    email: {
        type: DataTypes.STRING,
        allowNull: false
    },
    password: {
        type: DataTypes.STRING,
        allowNull: false
    }
})

module.exports = userAuth;
